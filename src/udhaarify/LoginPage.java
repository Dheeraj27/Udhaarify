    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        if(jTextField1.getText().toString().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter a username.");
            return;
        }
        if(jTextField1.getText().toString().contains(" ")){
            JOptionPane.showMessageDialog(null,"Username cannot contain spaces!");
            return;
        }
        try {
            username = jTextField1.getText();
            String query = "select pass_hint from user where username = ?";
            PreparedStatement p_hint = MySQLConnection.getConnection().prepareStatement(query);
            p_hint.setString(1, username);
            ResultSet rs = p_hint.executeQuery();
            if(!rs.next()){
                JOptionPane.showMessageDialog(null, "Enter correct username!");
                return;
            }
            
            hint = rs.getString(1);
            JOptionPane.showMessageDialog(null, "HINT: " + hint);        // TODO add your handling code here:
        } catch (SQLException ex) {
            System.out.println("Exception found");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         
        String password1 = new String(jPasswordField1.getPassword());
        if(password1.contains(" ")){
            JOptionPane.showMessageDialog(null, "Password cannot contain spaces!");
            return;
        }
        if(jTextField1.getText().isEmpty() || password1.equals("")){
            JOptionPane.showMessageDialog(null,"Username or password field cannot be empty!");
            return;
        }
        try {
            username = jTextField1.getText();
            String pass = new String(jPasswordField1.getPassword());
            int flag = 0;
            String query = "select * from user where username = ? and password = ?";
            PreparedStatement stmt = MySQLConnection.getConnection().prepareStatement(query);
            stmt.setString(1, username);
            stmt.setString(2, pass);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()){
                flag=1;
                password = pass;
            }
            if(flag==1){
                this.dispose();
                new Dashboard().setVisible(true);
            }
            else
            JOptionPane.showMessageDialog(null,"Username/password entered is wrong, retry!");
        } catch (SQLException ex) {
            System.out.println("Exception found");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

